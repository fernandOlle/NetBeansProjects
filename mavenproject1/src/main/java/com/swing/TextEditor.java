    private void btAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAbrirActionPerformed
        
        int resp = fc.showOpenDialog(null);
        
        if (resp == JFileChooser.APPROVE_OPTION){
            txtArea.setText("");
            File arquivo = fc.getSelectedFile();
            String path = arquivo.getAbsolutePath();
            try (BufferedReader br = new BufferedReader(new FileReader(path))) {
                String line = br.readLine();
                while(line != null){
                        if (txtArea != null) {
                            txtArea.setText(txtArea.getText() + line + "\n");
                        } else {
                            txtArea.setText(line + "\n");
                        }
                        line = br.readLine();
                }
            } catch (FileNotFoundException nf) {
                nf.printStackTrace();
                JOptionPane.showMessageDialog(this, "Arquivo NÃ£o Encontrado");
            } catch (IOException io) {
                io.printStackTrace();
                JOptionPane.showMessageDialog(this, "Erro ao Abrir");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cancelado");
        }
    }//GEN-LAST:event_btAbrirActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        fc.showSaveDialog(null);
        
        File arquivo = fc.getSelectedFile();
        String path = arquivo.getPath();
        
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(path + ".txt"))) {
            bw.write(txtArea.getText());
            JOptionPane.showMessageDialog(this, "Salvo");
        } catch (IOException io){
            io.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao Salvar");
        } 
    }//GEN-LAST:event_btSalvarActionPerformed

